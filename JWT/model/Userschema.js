import { types } from "mime-types";
import mongoose from "mongoose";

const userscema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export const users = mongoose.model("users", userscema);
